def normalize(text: str | None) -> str:
    if not text:
        return ""
    text = text.lower().replace("ё", "е")
    text = re.sub(r"\s+", " ", text)
    return text.strip()


def detect_region(address: str | None) -> Optional[str]:
    haystack = normalize(address)
    if not haystack:
        return None
    for region, keywords in REGION_KEYWORDS:
        if any(k in haystack for k in keywords):
            return region
    return None


def detect_city(address: str | None) -> Optional[str]:
    if not address:
        return None
    patterns = [
        r"(?:г\.|город)\s*([А-ЯA-ZӘІҢҒҮҰҚӨҺа-яa-zәіңғүұқөһ\-\s]+?)(?:,|$)",
        r"([А-ЯA-ZӘІҢҒҮҰҚӨҺа-яa-zәіңғүұқөһ\-\s]+?)\s*(?:қ\.|город|г\.)",
    ]
    for pattern in patterns:
        m = re.search(pattern, address, flags=re.IGNORECASE)
        if m:
            city = m.group(1).strip(" ,.;")
            if city and len(city) <= 40:
                return city
    return None
